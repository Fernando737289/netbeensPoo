    private void cargarRoles() {
        try {    
            control = new Controlador();       
            control.crearConexion();           
        
            String sql = "SELECT rol_id, rol_nombre FROM Roles";
            ResultSet rs = control.mandarQUERY(sql); 
            jComboBox1.removeAllItems();     

            while (rs.next()) {
                int id = rs.getInt("rol_id");
                String nombre = rs.getString("rol_nombre");
                jComboBox1.addItem(new RolItem(id, nombre)); 
            }

            rs.close(); 
        } catch (SQLException ex) {
           java.util.logging.Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

