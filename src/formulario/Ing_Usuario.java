    private void tfRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfRegistrarActionPerformed
        
        String password = new String(tfpwd.getPassword());
        RolItem rolSeleccionado = (RolItem) jComboBox1.getSelectedItem();
        
        try {
            control = new Controlador();
            control.crearConexion();
        } catch (SQLException ex){
            Logger.getLogger(Ing_Usuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        int idRol = rolSeleccionado.getId();
        
        String sql_ins = "insert into Usuarios (us_nom, us_pwd, us_nom_com, us_rol_id) values"
                + "('" + tfUsuario.getText() + "', '"+ password +"', '" + tfNomCom.getText() + "', " + idRol + ")";
        try {
            res = control.mandarSQL(sql_ins);
        } catch(SQLException ex) {
            Logger.getLogger(Ing_Usuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (res == - 1){
            JOptionPane.showMessageDialog(null, "ERROR");
        } else {
             JOptionPane.showMessageDialog(null, "Gravacion Exitosa");
        }
    }//GEN-LAST:event_tfRegistrarActionPerformed

