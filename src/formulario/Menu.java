    public Menu(String nombreUsuario, String rolUsuario) {
        initComponents();
        this.setLocationRelativeTo(null);


        //Esta linea de codigo agrega una imagen (IPCHILE ) y cambia el icono que esta en la carpeta imagenes
        java.net.URL imgURL = getClass().getResource("/imagenes/Ipchilelogo.png");
        if (imgURL != null) {
            ImageIcon icon = new ImageIcon(imgURL);
            lbIcons.setIcon(icon);
        } else {
            System.out.println("No se encontr√≥ la imagen en /imagenes/Ipchilelogo.png");
        }

        ImageIcon icon = new ImageIcon(getClass().getResource("/imagenes/icono.png"));
        this.setIconImage(icon.getImage());

        lbNom.setText(nombreUsuario);
        lbRol.setText(rolUsuario);
        this.rolUsuario = rolUsuario;
        this.nombreUsuario = nombreUsuario;
        
        rolUsuario = rolUsuario != null ? rolUsuario.trim().toLowerCase() : "";
  
        switch (rolUsuario) {
            case "admin":
                btBuscarUsu.setEnabled(true);
                btRegistrarUsu.setEnabled(true);
                btModificarUsu.setEnabled(true);
                btBuscarRecurso.setEnabled(true);
                btModificarRec.setEnabled(true);
                btRegistrarRecu.setEnabled(true);
                break;  
            case "soporte":
                btBuscarUsu.setEnabled(true);
                btRegistrarUsu.setEnabled(true);
                btModificarUsu.setEnabled(false);
                btBuscarRecurso.setEnabled(true);
                btModificarRec.setEnabled(false);
                btRegistrarRecu.setEnabled(true);
                break;
            case "usuario":
                btBuscarUsu.setEnabled(true);
                btRegistrarUsu.setEnabled(false);
                btModificarUsu.setEnabled(false);
                btBuscarRecurso.setEnabled(true);
                btModificarRec.setEnabled(false);
                btRegistrarRecu.setEnabled(false);
                break;
            default:
                JOptionPane.showMessageDialog(null, "Rol no encontrado");
                break;
        }
        
        
    }

