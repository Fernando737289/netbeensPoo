    void llenarTabla() throws SQLException {
        try {
            control = new Controlador();
            control.crearConexion();
        } catch (SQLException ex) {
            Logger.getLogger(Con_Recurso.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID RECURSO");
            model.addColumn("NOMBRE");
            model.addColumn("TIPO");
            model.addColumn("COSTO UNITARIO");
            tabRecurso.setModel(model);
            
            String sql = "SELECT ID_Recurso, Nombre, Tipo, Costo_Unitario FROM Recursos";
            
            rs = control.mandarQUERY(sql);
            
            String []fila = new String[4];
            
            while (rs.next()){
                fila[0] = rs.getString("ID_Recurso");
                fila[1] = rs.getString("Nombre");
                fila[2] = rs.getString("Tipo");
                fila[3] = rs.getString("Costo_Unitario");
                model.addRow(fila);
            }
            
            tabRecurso.setModel(model);
            
        } catch (SQLException ex) {
            Logger.getLogger(Con_Recurso.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

